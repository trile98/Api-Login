package PRACTICE.Api_Login.model;

import org.springframework.stereotype.Component;

@Component
public class UserRole {
	private int id;
	private String role;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getRole() {
		return role;
	}
	public void setRole(String role) {
		this.role = role;
	}
}
